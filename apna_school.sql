CREATE DATABASE apna_school;

use apna_school;

CREATE TABLE student  (
     id         VARCHAR(50),
     name       VARCHAR(50),
     age        VARCHAR(50),
     department VARCHAR(50),
     grade      INT,
     password VARCHAR(50)
  );

CREATE TABLE teacher  (
     id         VARCHAR(50),
     name       VARCHAR(50),
     age        VARCHAR(50),
     department VARCHAR(50),
     salary     INT,
     password VARCHAR(50)
  );
